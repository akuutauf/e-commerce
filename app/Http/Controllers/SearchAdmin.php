    {
        $empty = 0;
        $search = $request->search;
        $datas = Itm1::where('Itemname', 'LIKE', '%' . $search . '%')
            ->orWhere('MP_ProductName', 'LIKE', '%' . $search . '%')
            ->orWhere('Etalase', 'LIKE', '%' . $search . '%')
            ->get();
        return view('admin.search-admin', compact('datas', 'search', 'empty'));
    }

